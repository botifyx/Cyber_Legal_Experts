export const detectUserLanguage = (): LanguageCode => {
    try {
        const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone;
        
        if (timeZone.startsWith('Asia/Tokyo')) return 'ja';
        if (timeZone.includes('Paris') || timeZone.includes('Brussels')) return 'fr';
        if (timeZone.includes('Berlin') || timeZone.includes('Vienna') || timeZone.includes('Zurich')) return 'de';
        if (timeZone.includes('Madrid')) return 'es';
        if (timeZone.includes('Sao_Paulo') || timeZone.includes('Lisbon')) return 'pt';
        if (timeZone.includes('Amsterdam') || timeZone.includes('Rotterdam')) return 'nl'; 
        
        if (timeZone.startsWith('America/')) {
             if (timeZone.includes('Mexico') || timeZone.includes('Bogota') || timeZone.includes('Lima') || timeZone.includes('Santiago') || timeZone.includes('Argentina')) return 'es';
        }

        const navLang = navigator.language.split('-')[0];
        if (['en', 'es', 'fr', 'de', 'ja', 'pt', 'nl'].includes(navLang)) {
            return navLang as LanguageCode;
        }
    } catch (e) {
        console.warn("Language detection failed", e);
    }
    
    return 'en';
};

